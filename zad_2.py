# Zadanie 2: Funkcja mnożąca dwie liczby, które są całkowite
def multiply_two_numbers(a: int, b: int) -> int:
    """Mnoży dwie liczby całkowite i zwraca wynik."""
    return a * b

# Uruchomienie i wyświetlenie wyniku tego
wynik_mnozenia = multiply_two_numbers(7, 8)

print("Zadanie 2 ")
print(f"Wynik mnożenia 7 * 8 to: {wynik_mnozenia}")